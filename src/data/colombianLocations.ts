// Get unique departments sorted alphabetically
export const colombianDepartments = [...new Set(colombianMunicipalities.map(m => m.department))].sort();

// Get municipalities by department
export function getMunicipalitiesByDepartment(department: string): Municipality[] {
  return colombianMunicipalities
    .filter(m => m.department === department)
    .sort((a, b) => a.name.localeCompare(b.name));
}

// Format city name to Title Case
export function formatCityName(name: string): string {
  return name
    .toLowerCase()
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

// Search municipalities
export function searchMunicipalities(query: string): Municipality[] {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return [];
  
  return colombianMunicipalities
    .filter(m => 
      m.name.toLowerCase().includes(normalizedQuery) ||
      m.department.toLowerCase().includes(normalizedQuery)
    )
    .slice(0, 50); // Limit results
}
